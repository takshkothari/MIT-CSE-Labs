# 1. Assigning Values to Variables

counter = 100 # An integer assignment
miles = 1000.0 # A floating point
name = "John" # A string
print (counter)
print (miles)
print (name)


# 2. Multiple Assignment
# Python allows you to assign a single value to several variables simultaneously.
# For example:
a = b = c = 1
print (a, b, c)
a, b, c = 1, 2, "john"
print (a, b, c)

